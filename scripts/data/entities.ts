export interface EntityData {
  displayName?: string;
  internalId: number;
}

export function identifyEntity(entity: Entity): [string, string] {
  console.log(entity.typeId);
  return [entity.typeId, formatId(entity.typeId)];
}

export function countCollectedEntities(entities: string[]) {
  const builtInCount = entities.filter((b) => b in ALL_ENTITIES).length;
  return { collected: builtInCount, extra: entities.length - builtInCount, total: Object.keys(ALL_ENTITIES).length };
}

